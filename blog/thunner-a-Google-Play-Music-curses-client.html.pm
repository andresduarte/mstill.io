◊headline{thunner: a Google Play Music curses client}
◊publish-date[06 03 2013]
◊categories["thunner" "Google" "python"]

In December I released a curses client allowing playback of music stored/purchased with Google Play. The client is written in python and uses ◊link["http://www.simonmweber.com/"]{Simon Weber's} ◊link["https://github.com/simon-weber/Unofficial-Google-Music-API"]{unofficial Google Music API}. Details can be found on ◊link["https://github.com/mstill/thunner"]{the github page}.

It's still a ◊link["https://github.com/mstill/thunner/issues"]{work in progress} but I have been able to use it as my sole music player.

